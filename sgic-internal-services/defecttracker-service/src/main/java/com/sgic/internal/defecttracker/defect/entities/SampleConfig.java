package com.sgic.internal.defecttracker.defect.entities;

public class SampleConfig {
	private int id;
	private String name;

	public SampleConfig(int id, String name) {
		super();
		this.id = id;
		this.name = name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
